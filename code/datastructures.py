a = 5
b = 'hello'
c = 3.14

# Lists
lis = [1, 2, a, 'a'] # Note that there are two a's, the first is the value assigned to the variable 'a' and the second calls string 'a'

# Tuples
tup = (11, a, b, c, 'hello')

# Dictionary
dic = {'one' : 1, 'two' : 2, 'pi' : c}

# Accessing values in data structures
lis[3] # --> 5, as 5 is stored on the variable at index 3, which is 'a'
tup[4] # --> 'hello', value at index 4
dic['one'] # --> 1, as it the value to the key 'one'
